<p> My name is Charlotte Prior but I prefer to be called Lotty. I first got into photography a few years a go when I was given a polaroid camera. 
I am currently studying photography and am enjoying it greatly. My first photography project was called Identity.  
Since then I have completed projects about reflection.</p>
<p>During the course I have learnt many skills and feel that I am much more confident in my photography than I was at the beginning of the course. In the first year, the first project I studied was called ‘Identity’. This is where I took photographs that reinforce mine and others own specific identifications that basically summed you up. For example, I took a few photographs containing the items in my bag. This shows everyone’s identity as it has elements of who you are and the type of things you like. I then moved onto projects onto the project ‘Reflection’ where I learnt how to capture good quality photographs by controlling the amount of light used in the image to highlight the reflection, which is normally on shiny objects, such as metal. I then went onto my projects of ‘Light’ and ‘Movement’. For this project I learnt how to control the shutter speed to capture a fast movement. This is where I used a slow shutter speed to capture the movements of jumping. This captured the motion, making the image blurred. I got this influence from the photographer, Alex Majoli – (Referenced: http://www.alexmajoli.com/ )</p>
<p>After this, I then went onto studying ‘Fashion’ where I looked at photographers like Alex D. James  (Referenced: http://www.alex-james.co.uk/) This is where I took mainly studio photographs of my friends modelling for my photo shoots. I learnt how to control the amount of artificial lighting used in the photograph to create shadows and to get more effects in specific areas. </p>
<p>At the moment, I am currently focusing on Patterns within Nature in my second year. The photographers that I have looked at have varied from Peter Keetman (Referenced: http://de.wikipedia.org/wiki/Peter_Keetman)  to John Shaw (Referenced: http://www.johnshawphoto.com/) I first started looking at the patterns  found in day to day objects. I then moved onto looking at patterns relating to water and reflection. I’ve gone on to looking at Nikki Farquharson, (Referenced: http://www.nikkifarquharson.com/) a photographer that uses illustration patterns with studio, fashion photography. She has inspired me in my most recent work as I am now attempting to combine elements from this photographer with elements from other nature photographers that I have looked at, mainly involving plants, such as Andy Goldsworthy. All of my photographs are increasingly becoming more and more professional as I am exploring and editing my photographs using Photoshop Elements. </p>
<p>Alongside Photography, I am also studying my second year of Applied ICT and Combined English (Language and Literature). In my spare time I like to spend time with my family, friends and loved onces, go to the movies, draw and paint, and read books.  </p>
